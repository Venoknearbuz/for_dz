print("Система рейтингов продуктов \nСистема определяет качество продукта по рейтингу")
rating = int(input("Введите рейтинг продукта от 1 до 10"))

match rating:
    case 1| 2| 3| 4:
        print(f"Рэйтинг {rating} -> Плохой продукт, не рекомендую! ❌")
    case 5| 6| 7:
        print(f"Рэйтинг {rating} -> Средний продукт, можно попробывать 🤨")
    case 8| 9| 10:
        print(f"Рэйтинг {rating} -> Отличный продукт! Настоятельно рекомендую! ⭐")
    case _:
        print("ОШИБКА")

